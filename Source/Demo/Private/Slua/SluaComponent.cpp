#include "SluaComponent.h"
#include "LuaObject.h"

// Sets default values for this component's properties
USluaComponent::USluaComponent()
	:state("main")
{
	// Set this component to be initialized when the game starts, and to be ticked every frame.  You can turn these features
	// off to improve performance if you don't need them.
	PrimaryComponentTick.bCanEverTick = true;
	// ...
}

void USluaComponent::EndPlay(const EEndPlayReason::Type reason) {
    Super::EndPlay(reason);
    state.close();
}


// Called when the game starts
void USluaComponent::BeginPlay()
{
	Super::BeginPlay();

    state.init();
}


// Called every frame
void USluaComponent::TickComponent(float DeltaTime, ELevelTick TickType, FActorComponentTickFunction* ThisTickFunction)
{
	Super::TickComponent(DeltaTime, TickType, ThisTickFunction);

	state.tick(DeltaTime);
}

